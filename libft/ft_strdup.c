#include "includes/libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*new_str;

	i = 0;
	if (!(new_str = (char*)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (s1[i])
	{
		new_str[i] = s1[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
